package SubjectHeadings.Software;

public class ArithmeticOperator {
    public static void main(String[]args){
        int x=10;
        int y=4;
        System.out.println("Result" +(x+y));
        System.out.println("Result" +(x-y));
        System.out.println("Result" +(x*y));
        System.out.println("Result" +(x/y));
        System.out.println("Result" +(x%y));



        System.out.println("Result" +((double)x/y));


    }


}

