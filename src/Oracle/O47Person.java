package Oracle;

public class O47Person {
    private String name;
    private int age;

    public O47Person(String n,int a){
        setName (n);
        setAge (a);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}

